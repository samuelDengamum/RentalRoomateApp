export type AppLanguage = 'en' | 'fr' | 'ar';
export type AppCurrency = 'RWF' | 'USD' | 'EUR';

export interface IntlPreferences {
  language: AppLanguage;
  locale: string;
  currency: AppCurrency;
}

const STORAGE_KEYS = {
  language: 'intl.language',
  locale: 'intl.locale',
  currency: 'intl.currency'
};

const DEFAULTS: IntlPreferences = {
  language: 'en',
  locale: 'en-RW',
  currency: 'RWF'
};

export const LANGUAGE_OPTIONS: Array<{ value: AppLanguage; label: string; locale: string; short: string; dir: 'ltr' | 'rtl' }> = [
  { value: 'en', label: 'English', locale: 'en-RW', short: 'EN', dir: 'ltr' },
  { value: 'fr', label: 'Francais', locale: 'fr-RW', short: 'FR', dir: 'ltr' },
  { value: 'ar', label: 'Arabic (Saudi)', locale: 'ar-SA', short: 'AR', dir: 'rtl' }
];

export const getLanguageMeta = (language: AppLanguage) => {
  return LANGUAGE_OPTIONS.find((item) => item.value === language) || LANGUAGE_OPTIONS[0];
};

export const CURRENCY_OPTIONS: Array<{ value: AppCurrency; label: string }> = [
  { value: 'RWF', label: 'RWF (Rwandan Franc)' },
  { value: 'USD', label: 'USD (US Dollar)' },
  { value: 'EUR', label: 'EUR (Euro)' }
];

export const getIntlPreferences = (): IntlPreferences => {
  const language = (localStorage.getItem(STORAGE_KEYS.language) as AppLanguage | null) || DEFAULTS.language;
  const locale = localStorage.getItem(STORAGE_KEYS.locale) || DEFAULTS.locale;
  const currency = (localStorage.getItem(STORAGE_KEYS.currency) as AppCurrency | null) || DEFAULTS.currency;
  return { language, locale, currency };
};

export const saveIntlPreferences = (prefs: Partial<IntlPreferences>) => {
  if (prefs.language) {
    localStorage.setItem(STORAGE_KEYS.language, prefs.language);
    const match = LANGUAGE_OPTIONS.find((item) => item.value === prefs.language);
    if (match) {
      localStorage.setItem(STORAGE_KEYS.locale, match.locale);
      document.documentElement.lang = prefs.language;
      document.documentElement.dir = match.dir;
    }
  }

  if (prefs.locale) {
    localStorage.setItem(STORAGE_KEYS.locale, prefs.locale);
  }

  if (prefs.currency) {
    localStorage.setItem(STORAGE_KEYS.currency, prefs.currency);
  }
};

export const formatLocalizedNumber = (value?: number): string => {
  const normalized = Number.isFinite(value as number) ? Number(value) : 0;
  const { locale } = getIntlPreferences();
  return new Intl.NumberFormat(locale).format(normalized);
};

export const formatCurrencyLabel = (value?: number, currency?: AppCurrency): string => {
  const normalized = Number.isFinite(value as number) ? Number(value) : 0;
  const prefs = getIntlPreferences();
  const currencyCode = currency || prefs.currency;
  return `${currencyCode} ${formatLocalizedNumber(normalized)}`;
};

